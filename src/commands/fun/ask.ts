export const command = new BaseCommand({
    name: "ask",
    description: "Правда ли...",
    options: [
        {
            name: "text",
            description: "Вопрос",
            type: ApplicationCommandOptionType.String,
            isRequired: true,
        },
    ],
    run: async (ctx) => {
        if (!ctx.options.text.value) {
            ctx.meta.tags.push("empty text");

            return ctx.interaction.reply({
                content: `<@${ctx.interaction.user.id}> ты че еблан?`,
            });
        }

        const answer =
            ASK_ANSWERS[Math.floor(Math.random() * ASK_ANSWERS.length)];

        const question = ctx.options.text.value.endsWith("?")
            ? ctx.options.text.value
            : `${ctx.options.text.value}?`;

        return ctx.interaction.reply({
            embeds: [
                {
                    description: `**${question}**\n` + `\n` + `${answer}`,
                    color: 13748767,
                },
            ],
        });
    },
});
